// Build enriched personas from skills data
function buildPersona(id: string, personality: string): string {
  const skill = agentSkills[id];
  if (!skill) return personality;
  let persona = `${personality}\n\n## 직무 (Core Role)\n${skill.coreRole}\n\n## 보유 스킬\n${skill.skills.map(s => `- ${s}`).join('\n')}`;
  if (skill.successMetrics?.length) {
    persona += `\n\n## 성공 기준 (Success Metrics)\n${skill.successMetrics.map(s => `- ${s}`).join('\n')}`;
  }
  if (skill.criticalRules?.length) {
    persona += `\n\n## 절대 규칙 (Critical Rules)\n${skill.criticalRules.map(s => `-  ${s}`).join('\n')}`;
  }
  persona += `\n\n위 스킬을 기반으로 전문적이고 구체적인 답변을 해라. 질문이 스킬 범위 내라면 프레임워크와 방법론을 활용해서 답변해라. 범위 밖이면 담당 에이전트를 추천해라. 성공 기준을 항상 의식하고, 절대 규칙을 위반하지 마라.`;
  return persona;
}

