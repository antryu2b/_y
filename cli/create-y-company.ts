#!/usr/bin/env node

/**
 * create-y-company — Interactive CLI to generate a company.yml
 *
 * Usage: npx ts-node cli/create-y-company.ts
 *   or:  node dist/cli/create-y-company.js
 *
 * Generates a valid company.yml matching the _y Holdings schema.
 * Uses only Node built-in readline — no external dependencies.
 */

import * as readline from 'readline';
import * as fs from 'fs';
import * as path from 'path';

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
});

function ask(question: string, defaultVal?: string): Promise<string> {
  const suffix = defaultVal ? ` (${defaultVal})` : '';
  return new Promise((resolve) => {
    rl.question(`${question}${suffix}: `, (answer) => {
      resolve(answer.trim() || defaultVal || '');
    });
  });
}

function askNumber(question: string, defaultVal: number): Promise<number> {
  return ask(question, String(defaultVal)).then((v) => {
    const n = parseInt(v, 10);
    return isNaN(n) ? defaultVal : n;
  });
}

function askChoice(question: string, choices: string[], defaultVal: string): Promise<string> {
  const choiceStr = choices.map((c) => (c === defaultVal ? `[${c}]` : c)).join(' / ');
  return ask(`${question} (${choiceStr})`, defaultVal).then((v) => {
    return choices.includes(v) ? v : defaultVal;
  });
}

